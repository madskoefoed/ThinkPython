# Glossary
# interface design: A process for designing the interface of a function,
# which includes the parameters it should take.

# canvas: A window used to display graphical elements including lines,
# circles, rectangles, and other shapes.

# encapsulation: The process of transforming a sequence of statements
# into a function definition.

# generalization: The process of replacing something unnecessarily
# specific (like a number) with something appropriately general
# (like a variable or parameter).

# keyword argument: An argument that includes the name of the parameter.

# refactoring: The process of modifying a working program to improve
# function interfaces and other qualities of the code.

# development plan: A process for writing programs.

# docstring: A string that appears at the top of a function definition
# to document the function's interface.

# multiline string: A string enclosed in triple quotes that can span
# more than one line of a program.

# precondition: A requirement that should be satisfied by the caller
# before a function starts.

# postcondition: A requirement that should be satisfied by the function
# before it ends.